// Declarando uma variável do tipo string com um valor numérico
let numeroTexto: string = "10";

// Convertendo a string para número usando a função Number()
let numeroConvertido: number = Number(numeroTexto);

// Imprimindo o valor e o tipo da variável convertida no console
console.log("Valor convertido:", numeroConvertido);
console.log("Tipo da variável convertida:", typeof numeroConvertido);
